/****************************************************************************	  
//  功能描述   : OLED 4接口演示例程(51系列)
//              说明: 
//              ------------------------------------------------------------
//              GND    电源地
//              VCC  接5V或3.3v电源
//              D0   接PA6（SCL）
//              D1   接PA7（SDA）
//              RES  接PA4
//              DC   接PA5
//              CS   接PA3               

****************************************************************************/

#include "delay.h"
#include "sys.h"
#include "oled.h"
#include "bmp.h"
#include "stm32f4xx.h"
 int main(void)
 {	u8 t;
		delay_init(RCC_SYSCLK_Div2);	    	 //延时函数初始化	  
		NVIC_PriorityGroupConfig(NVIC_PriorityGroup_2); 	 //设置NVIC中断分组2:2位抢占优先级，2位响应优先级 	LED_Init();			     //LED端口初始化
		OLED_Init();			//初始化OLED  
		OLED_Clear(); 
		t=' ';
	while(1) 
	{		
		OLEDdisplay();
		delay_ms(6000);
		OLED_Clear();
		OLED_ShowString(0,3,(unsigned char*)"1.3' OLED TEST");
		OLED_ShowString(0,6,(unsigned char*)"ASCII:");  
		OLED_ShowString(63,6,(unsigned char*)"CODE:");  
		OLED_ShowChar(48,6,t);//显示ASCII字符	   
		t++;
		if(t>'~')t=' ';
		OLED_ShowNum(103,6,t,3,16);//显示ASCII字符的码值 	
		delay_ms(2000);
		OLED_Clear();
		OLED_DrawBMP(0,0,128,8,BMP1);  //图片显示(图片显示慎用，生成的字表较大，会占用较多空间，FLASH空间8K以下慎用)
		delay_ms(2000);



	}	  
	
}

